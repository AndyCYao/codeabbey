def distance(x1, y1, x2, y2, x3, y3):
    a = ((x2 - x1) ** 2 + (y2 - y1) ** 2) ** .5
    b = ((x3 - x2) ** 2 + (y3 - y2) ** 2) ** .5
    c = ((x1 - x3) ** 2 + (y1 - y3) ** 2) ** .5
    return abs(a), abs(b), abs(c)


def heron_method(a, b, c):
    s = (a + b + c) / 2
    area = (s * (s - a) * (s - b) * (s - c)) ** .5
    return round(area, 2)


