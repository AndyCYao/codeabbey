def find_type(s1, s2, h1):
    real_h = (s1 ** 2 + s2 ** 2) ** .5

    if real_h > h1:
        return "A"
    elif real_h < h1:
        return "O"
    elif real_h == h1:
        return "R"
    else:
        return "Error"

